package oneToManyAssociationInJava;

public class Employees
{
	String empName;
	String empAdd;
	int empAge;
	int empId;
	
	public Employees(String empName, String empAdd, int empAge, int empId) {
		super();
		this.empName = empName;
		this.empAdd = empAdd;
		this.empAge = empAge;
		this.empId = empId;
	}
	
}
